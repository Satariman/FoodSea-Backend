def infer_packaging(product: ProductRow) -> dict[str, object]:
    specific = specific_packaging(product)
    if specific is not None:
        return specific

    haystack = f"{product.subcategory} {product.name}"
    for pattern, (form, description, colors) in PACKAGING_RULES:
        if pattern.search(haystack):
            return {
                "packaging_type": form,
                "packaging_description": description,
                "category_palette": colors,
            }
    category_haystack = f"{product.category} {product.name}"
    for pattern, (form, description, colors) in PACKAGING_RULES:
        if pattern.search(category_haystack):
            return {
                "packaging_type": form,
                "packaging_description": description,
                "category_palette": colors,
            }
    return {
        "packaging_type": "стандартная товарная упаковка",
        "packaging_description": "упаковка должна соответствовать товарной категории, с крупным названием, брендом и читаемым весом",
        "category_palette": ["белый", "нейтральный", "акцентный цвет категории"],
    }


def brand_profile_base(brand: str, products: list[ProductRow], audit: dict[str, dict[str, str]]) -> dict[str, object]:
    curated = CURATED_BRANDS.get(brand, {})
    categories = Counter(p.category or "Без категории" for p in products)
    subcategories = Counter(p.subcategory or "Без подкатегории" for p in products)
    packaging = Counter(infer_packaging(p)["packaging_type"] for p in products)

    reference_rows = [
        audit.get(p.product_id, {})
        for p in products
        if audit.get(p.product_id, {}).get("status") == "likely_correct"
    ]
    reference_titles = [r.get("assigned_title", "") for r in reference_rows if r.get("assigned_title")]

    if curated:
        colors = curated["colors"]
        style = curated["style"]
        confidence = curated["confidence"]
        sources = curated.get("sources", [])
    else:
        common_packaging = packaging.most_common(1)[0][0] if packaging else "типовая упаковка"
        inferred_colors = infer_packaging(products[0])["category_palette"] if products else ["белый", "акцентный цвет"]
        colors = inferred_colors
        style = (
            f"бренд встречается в категории {categories.most_common(1)[0][0] if categories else 'каталог'}; "
            f"использовать {common_packaging}, крупное название бренда, читаемый вес и визуальный код основной подкатегории"
        )
        confidence = "inferred_from_catalog"
        sources = []

    return {
        "brand": brand,
        "product_count": len(products),
        "top_categories": [{"name": name, "count": count} for name, count in categories.most_common(8)],
        "top_subcategories": [{"name": name, "count": count} for name, count in subcategories.most_common(12)],
        "common_packaging_types": [{"type": name, "count": count} for name, count in packaging.most_common(8)],
        "brand_colors": colors,
        "visual_style": style,
        "confidence": confidence,
        "sources": list(sources),
        "local_reference_titles": reference_titles[:12],
    }


def extended_description(product: ProductRow, brand_profile: dict[str, object], packaging: dict[str, object], reference_title: str) -> str:
    def sentence(value: str) -> str:
        value = value.strip()
        if not value:
            return value
        return value if value.endswith((".", "!", "?")) else value + "."

    colors = ", ".join(str(color) for color in brand_profile["brand_colors"])
    parts = [
        sentence(f"{product.name} выглядит как отдельный студийный товар на светлом фоне"),
        sentence(f"Форма упаковки: {packaging['packaging_type']}"),
        sentence(str(packaging["packaging_description"])),
        sentence(f"Брендовая визуальная система: {brand_profile['visual_style']}"),
        sentence(f"Основные цвета для генерации: {colors}"),
    ]
    if product.weight:
        parts.append(sentence(f"На лицевой стороне обязательно указать вес или объем: {product.weight}"))
    if reference_title:
        parts.append(sentence(f"Локальный визуальный ориентир по текущему товару: {reference_title}"))
    if product.description:
        parts.append(sentence(f"Смысл товара из каталога: {product.description}"))
    parts.append("Композиция: фронтальный ракурс, мягкий студийный свет, без рук, полок магазина и лишних предметов.")
    return " ".join(parts)


def product_prompt(product: ProductRow, brand_profile: dict[str, object], audit_row: dict[str, str]) -> dict[str, object]:
    packaging = infer_packaging(product)
    status = audit_row.get("status", "unknown")
    reference_title = audit_row.get("assigned_title", "")
    visual_brand = canonical_visual_brand(product)
    brand_norm = product.brand.lower().replace("ё", "е").strip()
    reference_norm = reference_title.lower().replace("ё", "е")
    trusted_reference_title = (
        reference_title
        if status == "likely_correct" and (not brand_norm or brand_norm in reference_norm)
        else ""
    )
    reference_note = (
        f"Ориентир по локальному каталогу: {trusted_reference_title}. "
        if trusted_reference_title
        else ""
    )
    colors = ", ".join(str(color) for color in brand_profile["brand_colors"])
    prompt = (
        f"Фотореалистичное студийное фото товара '{product.name}'. "
        f"Бренд: {visual_brand}, фирменные цвета: {colors}. "
        f"Тип упаковки: {packaging['packaging_type']}; {packaging['packaging_description']}. "
        f"Визуальный стиль бренда: {brand_profile['visual_style']}. "
        f"{reference_note}"
        f"На лицевой стороне должны быть читаемы название товара, бренд и вес/объем '{product.weight}'. "
        f"Белый или светлый фон, ровный мягкий свет, фронтальный ракурс, без посторонних предметов."
    )
    extended = extended_description(product, brand_profile, packaging, trusted_reference_title)
    return {
        "product_id": product.product_id,
        "name": product.name,
        "brand": product.brand,
        "category": product.category,
        "subcategory": product.subcategory,
        "weight": product.weight,
        "current_image_status": status,
        "current_image_url": product.image_url,
        "reference_title": reference_title,
        "packaging_type": packaging["packaging_type"],
        "packaging_description": packaging["packaging_description"],
        "brand_colors": brand_profile["brand_colors"],
        "brand_visual_style": brand_profile["visual_style"],
        "extended_description_ru": extended,
        "visual_confidence": brand_profile["confidence"],
        "prompt_ru": prompt,
        "negative_prompt_ru": "не добавлять чужие логотипы кроме указанного бренда, не менять тип продукта, не делать фантазийную упаковку, не добавлять руки, витрину или интерьер магазина",
    }


def main() -> None:
    REPORTS_DIR.mkdir(parents=True, exist_ok=True)
    products = load_products()
    audit = load_audit()

    by_brand: dict[str, list[ProductRow]] = defaultdict(list)
    for product in products:
        by_brand[canonical_visual_brand(product)].append(product)

    brand_profiles = {
        brand: brand_profile_base(brand, rows, audit)
        for brand, rows in sorted(by_brand.items(), key=lambda item: item[0].lower())
    }

    product_profiles = [
        product_prompt(product, brand_profiles[canonical_visual_brand(product)], audit.get(product.product_id, {}))
        for product in products
    ]

    BRAND_OUTPUT.write_text(json.dumps(brand_profiles, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    with PRODUCT_JSONL_OUTPUT.open("w", encoding="utf-8") as file:
        for row in product_profiles:
            file.write(json.dumps(row, ensure_ascii=False) + "\n")

    csv_fields = [
        "product_id",
        "name",
        "brand",
        "category",
        "subcategory",
        "weight",
        "current_image_status",
        "packaging_type",
        "brand_colors",
        "расширенное описание",
        "visual_confidence",
        "prompt_ru",
        "negative_prompt_ru",
    ]
    with PRODUCT_CSV_OUTPUT.open("w", encoding="utf-8", newline="") as file:
        writer = csv.DictWriter(file, fieldnames=csv_fields)
        writer.writeheader()
        for row in product_profiles:
            flat = dict(row)
            flat["brand_colors"] = ", ".join(str(color) for color in row["brand_colors"])
            flat["расширенное описание"] = row["extended_description_ru"]
            writer.writerow({field: flat.get(field, "") for field in csv_fields})

    status_counts = Counter(row["current_image_status"] for row in product_profiles)
    confidence_counts = Counter(row["visual_confidence"] for row in product_profiles)
    summary = [
        "# Product Visual Generation Profiles",
        "",
        f"- Products: {len(product_profiles)}",
        f"- Brands: {len(brand_profiles)}",
        f"- Brand profiles: `{BRAND_OUTPUT}`",
        f"- Product JSONL: `{PRODUCT_JSONL_OUTPUT}`",
        f"- Product CSV: `{PRODUCT_CSV_OUTPUT}`",
        "",
        "## Current Image Status",
        "",
    ]
    summary.extend(f"- {name}: {count}" for name, count in sorted(status_counts.items()))
    summary.extend(["", "## Visual Confidence", ""])
    summary.extend(f"- {name}: {count}" for name, count in sorted(confidence_counts.items()))
    summary.extend(
        [
            "",
            "## Notes",
            "",
            "- `curated_known_brand` and `local_reference_curated` profiles contain hand-authored brand cues.",
            "- `inferred_from_catalog` profiles are category-based fallbacks for rare brands without reliable local visual references.",
            "- Product prompts intentionally describe packaging and brand colors, but avoid asking the generator to copy an exact copyrighted label.",
        ]
    )
    SUMMARY_OUTPUT.write_text("\n".join(summary) + "\n", encoding="utf-8")

    print(f"wrote {BRAND_OUTPUT}")
    print(f"wrote {PRODUCT_JSONL_OUTPUT}")
    print(f"wrote {PRODUCT_CSV_OUTPUT}")
    print(f"wrote {SUMMARY_OUTPUT}")


if __name__ == "__main__":
    main()
