@dataclass(frozen=True)
class ProductRow:
    product_id: str
    name: str
    brand: str
    subcategory: str
    category: str
    weight: str
    description: str
    image_url: str


def canonical_visual_brand(product: ProductRow) -> str:
    """Return the brand name to use for visual generation.

    Some catalog rows store umbrella brands (e.g. Coca-Cola) while the product
    name clearly indicates a sub-brand with its own packaging system (e.g. BonAqua).
    """
    name = (product.name or "").lower().replace("ё", "е")
    brand = (product.brand or "").strip()
    if brand == "Coca-Cola" and "bonaqua" in name:
        return "Bonaqua"
    return brand or "Без бренда"


